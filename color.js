// 2. The Processor Function
function generateCSVChunks(links, batchSize = 50) {
    
    // Step A: Clean and Format the Data
    const cleanRows = links
        .filter(link => link && link.startsWith("/category/")) // Remove "#" and empty items
        .map(source => {
            // Create the Target URL
            const target = source.replace("/category/", "/games/");
            
            // Return in CSV format: source,target
            // We strip the domain to make it relative (safer for imports)
            return `${source},${target}`;
        });

    console.log(`Total valid redirects found: ${cleanRows.length}`);

    // Step B: Split into Chunks and Output
    for (let i = 0; i < cleanRows.length; i += batchSize) {
        const chunk = cleanRows.slice(i, i + batchSize);
        
        console.log(`\n⬇️ --- COPY BELOW THIS LINE (BATCH ${Math.floor(i / batchSize) + 1}) --- ⬇️`);
        console.log("source,target"); // CSV Header required by most plugins
        console.log(chunk.join("\n"));
        console.log(`⬆️ --- END BATCH ${Math.floor(i / batchSize) + 1} --- ⬆️\n`);
    }
}

